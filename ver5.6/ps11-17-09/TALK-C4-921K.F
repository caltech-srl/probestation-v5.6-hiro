: openbinfile 
	conhndl start: newbinfile dup c@
	if count r/w create-file 0=
		if binfileid ! -1 binflg !   
		else cr ." file not created" drop
		then
	else drop
	then ;

: closebinfile 
	binfileid @ close-file drop 0 binflg ! 
	." binary file closed" ;

: openlogfile 
	conhndl start: newlogfile dup c@
	if count r/w create-file 0=
		if logfileid ! -1 logflg !   
		else cr ." file not created" drop
		then
	else drop
	then ;

: closelogfile
	logcnt @ if logbuf logcnt @ logfileid @ write-file drop then
	logfileid @ close-file drop 0 logflg ! 
	." log file closed" ;

: key>talk												\ keystrokes are typically just sent
		?key												\ out usb to misc, 0xbut some keys
		if dup k_ESC =									\ invoke file operations
				if drop -1
         	else 
			dup ^A =
					if drop sndtxt 0
					else
			dup ^B =
						if drop
							binflg @
							if closebinfile
							else openbinfile
							then 0
						else
			dup ^G =
							if drop 
								logflg @
								if closelogfile
								else openlogfile
								then 0
							else
								Com4emit 0
							then
						then
					then
			then
 		else 0
		then ;

: talk													\ terminal talker, with embedded data
	0 >state ! 0 binlim ! 0 bincnt ! 0 binflg !
	open_Com4
	cr
	cr ."   ESC - exit"
	cr ."  COM4 - baud=921600 parity=N data=8 stop=2"
	cr ."    ^A - download and compile text file" 
	cr ."    ^B - open or close binary data file"
	cr ."    ^G - open or close log file" cr
	13 Com4emit
 	begin
		Com4>talk
		key>talk
		1 MS 
	until Com4hndl close-file drop ;

