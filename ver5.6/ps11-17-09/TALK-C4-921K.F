variable >state	\ 0 => command response; 1 => binary just detected; 2 => binary
variable binlim	\ binary data packet begins with ^Z
variable bincnt	\ next byte is "byte count - 1"
variable binflg	\ 0 => don't write to binary file; -1 => do write
1024 constant loglim
variable logcnt
variable logflg

create binbuf 256 allot
create logbuf loglim allot

: wbinfile binbuf binlim @ binfileid @ write-file drop ;
: wlogfile logbuf loglim logfileid @ write-file drop ;
: emit>log
	logflg @
	if logbuf logcnt @ + c! 1 logcnt +! 
		logcnt @ loglim = if wlogfile 0 logcnt ! then
	else drop 
	then ;
: type>log 0 do dup i + c@ emit>log loop drop ;
: >file&screen		\ time inserted after linfeed, ^Z begins binary data, 
						\ ^Z is followed by single byte = bytes of binary data -1
	Com4_bytes_read @ 0 
	do 
		>state @
		dup 0 = 	if	Com4readbuf i + c@ 
						dup linefeed = 	
								if 
									dup emit emit>log
									.time time$ count type>log
									space 32 emit>log
								else
						dup ^Z = 			
									if drop 1 >state ! 
									else
										dup emit emit>log					
									then 
								then 
						drop
					else
		    1 = 		if	Com4readbuf i + c@ 1+ binlim ! 2 >state ! 
						else
							Com4readbuf i + c@ binbuf bincnt @ + !
							1 bincnt +! 
							bincnt @ binlim @ = 
							if 0 >state ! 0 bincnt !
								binflg @ if wbinfile then
							then
						then 
					then
	loop ;

: Com4_?keys
			Com4readbuf 2000 Com4Hndl read-file
			dup
			if	dup
				." Error code = " . space
				abort" Failed to read port"
			else drop
			then
         dup Com4_bytes_read !
			dup charmax @ max charmax !
			if -1 else 0 then ;

: Com4>talk 
	Com4_?keys 
	if 
		>file&screen 
	then ; 	

